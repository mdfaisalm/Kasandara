package com.kassandra.apps.flutter.newDummy;

public class ParentCalculatorHelper {
    public int calculateAddition(int firstNumber, int secondNumber) {
        int result = firstNumber + secondNumber;
        return result;
    }

    public int calculateAddition(int firstNumber, int secondNumber, int thirdNumber) {
        int result = firstNumber + secondNumber + thirdNumber;
        return result;
    }

    public int calculateSubtraction(int firstNumber, int secondNumber) {
        int result = firstNumber - secondNumber;
        return result;
    }

}
